// ===== TYPE DEFINITIONS =====

export interface ExerciseConstraints {
  requiresIliopsoas: boolean;
  maxHipFlexion: number;
  requiresWeightBearing: boolean;
}

export interface VariantSuperset {
  title: string;
  sets: string;
  instruction: string;
  safety: string;
  note?: string;
}

export interface MachineVariant {
  id: string;
  label: string;
  icon: string;
  description: string;
  setupCues: string[];
  superset?: VariantSuperset;
}

export interface Exercise {
  id: string;
  name: string;
  requires: string[];
  category: string;
  sets: [string, string][];
  rest: number;
  setup: string;
  execution: string;
  nwbCues: string;
  why: string;
  safety: "safe" | "caution" | "danger";
  swaps: string[];
  visual?: string;
  diagram?: string;
  tempo?: string;
  amp?: string[];
  phase?: number;
  tier?: number;
  machineVariants?: MachineVariant[];
  cableSuperset?: boolean;
  constraints: ExerciseConstraints;
}

export interface EquipmentItem {
  name: string;
  icon: string;
  category: string;
}

export interface Workout {
  title: string;
  icon: string;
  color: string;
  hevy?: string;
  exercises: string[];
  removed: { name: string; reason: string }[];
}

export interface ScheduleDay {
  d: string;
  t: string;
  i: string;
  c: string;
}

export interface Phase {
  weeks: string;
  name: string;
  color: string;
  desc: string;
}

